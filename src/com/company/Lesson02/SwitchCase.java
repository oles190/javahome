package com.company.Lesson02;

public class SwitchCase {
    public static void main(String[] args) {

        int a = 10;
        switch (a) {
            case 2:{
                System.out.println("2");
                break;
            }
            case 4:{
                System.out.println("4");
                break;
            }
            case 110:{
                System.out.println("10");
                break;
            }
            default:{
                System.out.println("i dont know ");
            }

        }


    }
}